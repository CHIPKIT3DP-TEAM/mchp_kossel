#define _NVSTORAGE_C_
#include "nvstorage.h"
////////////////////////////////////////////////////////////////////////////////

MCP98244_EE seeprom_ =
{
    .i2c = MAIN_I2C,
    .address = MCP98244_I2C_EE_BASE_ADDRESS
};
MCP98244_EE_HANDLE seeprom = &seeprom_;

////////////////////////////////////////////////////////////////////////////////

BOOL NVS_Initialize ()
{
    if ( MCP98244_EE_Initialize ( seeprom ) != TRUE )
        return ( FALSE );
    else
        return ( TRUE );
}

